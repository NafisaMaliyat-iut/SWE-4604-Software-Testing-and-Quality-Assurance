package org.example;

public class Math {
    public float add(float a, float b){
        return a + b;
    }

    public float substract(float a, float b){
        return a - b;
    }

    public float multiply(float a, float b){
        return a * b;
    }

    public float divide(float a, float b){
        return a / b;
    }
}